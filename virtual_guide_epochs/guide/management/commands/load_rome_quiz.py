from django.core.management.base import BaseCommand
from guide.models import QuizQuestion, Era

class Command(BaseCommand):
    help = 'Загрузка вопросов для викторины про Античный Рим'

    def handle(self, *args, **kwargs):
        # Найти эпоху "Античный Рим"
        try:
            rome = Era.objects.get(name="Античный Рим")
        except Era.DoesNotExist:
            self.stdout.write(self.style.ERROR('Эпоха "Античный Рим" не найдена!'))
            return

        # Список вопросов
        rome_quiz = [
            {
                "question": "Какой город стал центром Римской империи?",
                "options": ["Рим", "Афины", "Карфаген", "Александрия"],
                "correct": "Рим"
            },
            {
                "question": "Как назывался римский амфитеатр, известный как Колизей?",
                "options": ["Амфитеатр Флавиев", "Амфитеатр Августа", "Амфитеатр Траяна", "Амфитеатр Нерона"],
                "correct": "Амфитеатр Флавиев"
            },
            {
                "question": "Какой титул носил правитель Римской империи?",
                "options": ["Император", "Король", "Цезарь", "Консул"],
                "correct": "Император"
            },
            {
                "question": "Какой знаменитый римский полководец пересёк Рубикон?",
                "options": ["Гай Юлий Цезарь", "Гней Помпей", "Октавиан Август", "Марк Антоний"],
                "correct": "Гай Юлий Цезарь"
            },
            {
                "question": "Как назывались крупные римские дороги?",
                "options": ["Аппиева дорога", "Цезарева дорога", "Имперская дорога", "Августова дорога"],
                "correct": "Аппиева дорога"
            },
            {
                "question": "Какой язык был официальным в Римской империи?",
                "options": ["Латынь", "Греческий", "Арамейский", "Итальянский"],
                "correct": "Латынь"
            },
            {
                "question": "Как назывался римский совет старейшин?",
                "options": ["Сенат", "Форум", "Конвент", "Курия"],
                "correct": "Сенат"
            },
            {
                "question": "Какой день недели назван в честь римского бога войны?",
                "options": ["Вторник", "Четверг", "Пятница", "Суббота"],
                "correct": "Вторник"
            },
            {
                "question": "Как назывался период мира в Римской империи?",
                "options": ["Пакс Романа", "Великое умиротворение", "Мир Августа", "Золотой век"],
                "correct": "Пакс Романа"
            },
            {
                "question": "Какой римский бог считался покровителем торговли?",
                "options": ["Меркурий", "Марс", "Юпитер", "Вулкан"],
                "correct": "Меркурий"
            }
        ]

        # Добавление вопросов в базу данных
        for q in rome_quiz:
            QuizQuestion.objects.create(
                era=rome,
                question=q["question"],
                option1=q["options"][0],
                option2=q["options"][1],
                option3=q["options"][2],
                option4=q["options"][3],
                correct_answer=q["correct"]
            )

        self.stdout.write(self.style.SUCCESS('Вопросы для викторины про Античный Рим успешно добавлены!'))
