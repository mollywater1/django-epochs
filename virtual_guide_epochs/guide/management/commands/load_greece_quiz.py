from django.core.management.base import BaseCommand
from guide.models import QuizQuestion, Era

class Command(BaseCommand):
    help = 'Загрузка вопросов для викторины про Древнюю Грецию'

    def handle(self, *args, **kwargs):
        # Найти эпоху "Древняя Греция"
        try:
            greece = Era.objects.get(name="Древняя Греция")
        except Era.DoesNotExist:
            self.stdout.write(self.style.ERROR('Эпоха "Древняя Греция" не найдена!'))
            return

        # Список вопросов
        greece_quiz = [
            {
                "question": "Как назывался главный город-государство Древней Греции?",
                "options": ["Афины", "Спарта", "Фивы", "Коринф"],
                "correct": "Афины"
            },
            {
                "question": "Как назывался верховный бог в Древней Греции?",
                "options": ["Зевс", "Аид", "Посейдон", "Аполлон"],
                "correct": "Зевс"
            },
            {
                "question": "Кто написал \"Илиаду\" и \"Одиссею\"?",
                "options": ["Гомер", "Гесиод", "Софокл", "Эсхил"],
                "correct": "Гомер"
            },
            {
                "question": "Как назывался священный храм Афины на Акрополе?",
                "options": ["Парфенон", "Эрехтейон", "Храм Зевса", "Храм Аполлона"],
                "correct": "Парфенон"
            },
            {
                "question": "Как назывались древнегреческие спортивные соревнования?",
                "options": ["Олимпийские игры", "Агоны", "Гладиаторские бои", "Панэллинские игры"],
                "correct": "Олимпийские игры"
            },
            {
                "question": "Какой философ учил Александра Македонского?",
                "options": ["Аристотель", "Сократ", "Платон", "Демокрит"],
                "correct": "Аристотель"
            },
            {
                "question": "Как назывался великий военачальник Древней Греции?",
                "options": ["Александр Македонский", "Леонид", "Филипп II", "Перикл"],
                "correct": "Александр Македонский"
            },
            {
                "question": "Кто был богом подземного царства?",
                "options": ["Аид", "Зевс", "Посейдон", "Гермес"],
                "correct": "Аид"
            },
            {
                "question": "Как назывались люди, предсказавшие будущее в Дельфах?",
                "options": ["Оракулы", "Жрецы", "Прорицатели", "Друиды"],
                "correct": "Оракулы"
            },
            {
                "question": "Какой известный математик создал теорему о прямоугольных треугольниках?",
                "options": ["Пифагор", "Архимед", "Евклид", "Герон"],
                "correct": "Пифагор"
            }
        ]

        # Добавление вопросов в базу данных
        for q in greece_quiz:
            QuizQuestion.objects.create(
                era=greece,
                question=q["question"],
                option1=q["options"][0],
                option2=q["options"][1],
                option3=q["options"][2],
                option4=q["options"][3],
                correct_answer=q["correct"]
            )

        self.stdout.write(self.style.SUCCESS('Вопросы для викторины про Древнюю Грецию успешно добавлены!'))
